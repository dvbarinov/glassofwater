"""
Модуль расчёта суточной нормы воды.

Содержит функции для определения индивидуальной цели потребления воды
на основе пола, веса и уровня физической активности.
"""

def calculate_daily_water_goal(gender: int, weight_kg: int, activity_level: int) -> int:
    """
    Рассчитывает рекомендуемую суточную норму воды в миллилитрах.

    Формула:
        Базовая норма = вес (кг) × 30 мл
        Женщинам применяется коэффициент 0.9
        Активность добавляет бонус: низкая=0, средняя=300, высокая=600 мл

    Args:
        gender (int): Пол (0 = мужской, 1 = женский).
        weight_kg (int): Вес пользователя в килограммах (30–200).
        activity_level (int): Уровень активности (0 = низкий, 1 = средний, 2 = высокий).

    Returns:
        int: Рекомендуемая норма воды в мл (ограничена 1200–5000 мл).

    Пример:
        >>> calculate_daily_water_goal(0, 70, 1)
        2400
    """
    base = weight_kg * 35  # 30 мл на кг
    activity_bonus = {0: 0, 1: 300, 2: 600}[activity_level]

    # Женщинам немного меньше (по некоторым рекомендациям)
    if gender == 1:
        base = int(base * 0.9)

    total = base + activity_bonus
    return max(1200, min(5000, total))  # Ограничение разумных рамок
